        <h3>THE IDEA</h3>
        <p>MATA CMS is a lean but powerful content management 
            system (CMS) built on top of Yii, an open source, high-performance PHP framework designed for developing Web 2.0 applications.</p>
        <p>It takes a different approach from CMS’ like Wordpress or Drupal 
            in the fact it is ‘developer’ orientated and not ‘web designer’. What we mean is that it should be efficient for web developers and a good experience for the end user, not a tool focused on compensating for web designers who can’t code.</p>

